// Poseidon-BLS parameter generator (FIN-002).
//
// Generates round constants (Grain LFSR) and an MDS matrix (Cauchy) for Poseidon
// over the BLS12-381 SCALAR field `r`, for the widths Finnes needs (t = 2, 3, 6).
// Emits TWO byte-identical consumers so the SDK and the circuit hash the same:
//   - sdk/src/poseidon-params.ts          (TypeScript bigint constants)
//   - circuits/lib/poseidon_constants.circom (Circom functions)
//
// Provenance: "fresh, self-consistent" (chosen in design). This is NOT claimed to
// match any external Poseidon test vectors. Security rests on: alpha=5 is a
// permutation (gcd(5, r-1)=1), the MDS matrix is a Cauchy matrix (always MDS),
// and the round constants come from a Grain-style LFSR with rejection sampling.
//
// Regenerate with:  node scripts/gen-poseidon-params.mjs
// Regenerating changes the constants -> a parity-breaking event: re-run the
// ceremony and update the test vector (CLAUDE.md invariant #13).

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

// BLS12-381 scalar field modulus r.
const P = 0x73eda753299d7d483339d80809a1d80553bda402fffe5bfeffffffff00000001n;
const N = 255; // bit length of r (2^254 < r < 2^255)
const ALPHA = 5;
const RF = 8; // full rounds
const RP = { 2: 56, 3: 57, 6: 60 }; // partial rounds per width (standard 128-bit table)
const WIDTHS = [2, 3, 6];

// ---- field helpers ----
const mod = (a) => ((a % P) + P) % P;
function modpow(b, e, m) {
  b = mod(b);
  let r = 1n;
  while (e > 0n) {
    if (e & 1n) r = (r * b) % m;
    b = (b * b) % m;
    e >>= 1n;
  }
  return r;
}
const inv = (a) => modpow(a, P - 2n, P);

// ---- Grain LFSR ----
function bits(value, width) {
  const out = [];
  const v = BigInt(value);
  for (let i = width - 1; i >= 0; i--) out.push(Number((v >> BigInt(i)) & 1n));
  return out;
}

// 80-bit Fibonacci LFSR seeded with (field, sbox, n, t, R_F, R_P) + padding,
// warmed up 160 steps. Output bit = bit shifted out; feedback taps 0,13,23,38,51,62.
function makeLfsr(t) {
  const state = [
    ...bits(1, 2), // field = prime field GF(p)
    ...bits(0, 4), // sbox tag (exponentiation)
    ...bits(N, 12),
    ...bits(t, 12),
    ...bits(RF, 10),
    ...bits(RP[t], 10),
    ...Array(30).fill(1), // padding
  ];
  if (state.length !== 80) throw new Error(`bad init length ${state.length}`);
  const step = () => {
    const out = state[0];
    const nb = state[0] ^ state[13] ^ state[23] ^ state[38] ^ state[51] ^ state[62];
    state.shift();
    state.push(nb);
    return out;
  };
  for (let i = 0; i < 160; i++) step(); // warm up
  return step;
}

// One field element via rejection sampling: read N bits MSB-first; accept if < P.
function nextField(step) {
  for (;;) {
    let v = 0n;
    for (let i = 0; i < N; i++) v = (v << 1n) | BigInt(step());
    if (v < P) return v;
  }
}

function genWidth(t) {
  const step = makeLfsr(t);
  const nRounds = RF + RP[t];
  const C = [];
  for (let i = 0; i < nRounds * t; i++) C.push(nextField(step));
  // Cauchy MDS: x_i = i, y_j = t + j  =>  M[i][j] = 1 / (x_i + y_j).
  // x_i distinct, y_j distinct, x_i + y_j != 0  =>  matrix is MDS.
  const M = [];
  for (let i = 0; i < t; i++) {
    const row = [];
    for (let j = 0; j < t; j++) row.push(inv(mod(BigInt(i) + BigInt(t + j))));
    M.push(row);
  }
  return { C, M, nRounds };
}

const params = Object.fromEntries(WIDTHS.map((t) => [t, genWidth(t)]));

// ---- emit TypeScript ----
const tsBody = `// GENERATED by scripts/gen-poseidon-params.mjs - DO NOT EDIT.
// Poseidon-BLS parameters for the BLS12-381 scalar field. Byte-identical to
// circuits/lib/poseidon_constants.circom (invariant #13). Regenerate via:
//   node scripts/gen-poseidon-params.mjs

export const P = ${P}n;
export const ALPHA = ${ALPHA};
export const RF = ${RF};
export const RP: Record<number, number> = ${JSON.stringify(RP)};

export const C: Record<number, bigint[]> = {
${WIDTHS.map((t) => `  ${t}: [\n${params[t].C.map((c) => `    ${c}n,`).join('\n')}\n  ],`).join('\n')}
};

export const M: Record<number, bigint[][]> = {
${WIDTHS.map(
  (t) =>
    `  ${t}: [\n${params[t].M.map((row) => `    [${row.map((x) => `${x}n`).join(', ')}],`).join('\n')}\n  ],`,
).join('\n')}
};
`;
writeFileSync(join(ROOT, 'sdk/src/poseidon-params.ts'), tsBody);

// ---- emit Circom ----
const circomBody = `// GENERATED by scripts/gen-poseidon-params.mjs - DO NOT EDIT.
// Poseidon-BLS parameters (BLS12-381 scalar field). Byte-identical to
// sdk/src/poseidon-params.ts (invariant #13). Compile with --prime bls12381.
pragma circom 2.1.6;

function POSEIDON_RF_BLS() { return ${RF}; }

function POSEIDON_RP_BLS(t) {
${WIDTHS.map((t) => `    if (t == ${t}) { return ${RP[t]}; }`).join('\n')}
    return 0;
}

function POSEIDON_C_BLS(t) {
${WIDTHS.map(
  (t) => `    if (t == ${t}) {
        return [${params[t].C.join(', ')}];
    }`,
).join('\n')}
    return [0];
}

function POSEIDON_M_BLS(t) {
${WIDTHS.map(
  (t) => `    if (t == ${t}) {
        return [${params[t].M.map((row) => `[${row.join(', ')}]`).join(', ')}];
    }`,
).join('\n')}
    return [[0]];
}
`;
writeFileSync(join(ROOT, 'circuits/lib/poseidon_constants.circom'), circomBody);

console.log('Generated:');
console.log('  sdk/src/poseidon-params.ts');
console.log('  circuits/lib/poseidon_constants.circom');
for (const t of WIDTHS) console.log(`  t=${t}: ${params[t].nRounds} rounds, ${params[t].C.length} constants, ${t}x${t} MDS`);
